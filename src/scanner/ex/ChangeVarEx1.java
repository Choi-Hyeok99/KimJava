package scanner.ex;

public class ChangeVarEx1 {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;
        int temp ;

        temp = a; // temp : 10
        a = b; // a : 20
        b = temp; // b : 10

        System.out.println("a의 값:  "+ a + " " + "b의 값: " + b);






    }
}
